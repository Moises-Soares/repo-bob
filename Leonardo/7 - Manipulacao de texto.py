# Bem-vindos ao Tutorial de Manipulação de Strings em Python!

# O que é uma string?
# Uma string é simplesmente um pedaço de texto dentro de aspas.

# Exemplo 1: Concatenar (juntar) strings
# Vamos criar uma história juntando pedaços de texto.
parte1 = "Era uma vez um dragão"
parte2 = " que sabia programar em Python."
historia = parte1 + parte2
print(historia)

# Exemplo 2: Dividindo uma string
# Podemos dividir uma string em partes.
frase = "Olá, mundo, estou a aprender Python"
palavras = frase.split(", ")
print("As palavras na frase são:", palavras)

# Exemplo 3: Substituindo partes de uma string
# E se quisermos mudar uma palavra?
frase_nova = frase.replace("Olá", "Adeus")
print("A frase mudou para:", frase_nova)

# Exemplo 4: Formatando strings
# Podemos inserir variáveis em strings facilmente.
nome = "Ana"
saudacao = f"Olá, {nome}! Bem-vinda ao mundo do Python."
print(saudacao)

