# Agora que já sabes o que são variáveis, vamos aprender sobre diferentes tipos de dados e algumas operações básicas.

# Tipos de Dados em Python
# Existem vários tipos de dados em Python, mas vamos começar com os mais comuns: int, float, str e bool.

# int (números inteiros)
# Exemplo: 5, -3, 42
numero_inteiro = 10

# float (números decimais)
# Exemplo: 3.14, -0.001
numero_decimal = 5.7

# str (texto, ou string)
# Exemplo: "Olá, mundo!"
texto = "Python é divertido"

# bool (booleano, que pode ser True ou False)
# Exemplo: True, False
valor_booleano = True

# Operações com Números
# Vamos fazer algumas operações matemáticas básicas.

# Adição
soma = 5 + 3
print("Resultado da soma:", soma)

# Subtração
diferenca = 5 - 3
print("Resultado da subtração:", diferenca)

# Multiplicação
produto = 5 * 3
print("Resultado da multiplicação:", produto)

# Divisão
quociente = 5 / 3
print("Resultado da divisão:", quociente)

# Divisão Inteira
divisao_inteira = 5 // 3
print("Resultado da divisão inteira:", divisao_inteira)

# Módulo (resto da divisão)
resto = 5 % 3
print("Resto da divisão:", resto)

# Exercício 1: Tenta fazer as tuas próprias operações matemáticas com números diferentes.
