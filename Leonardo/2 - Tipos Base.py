# Trabalhando com Strings
# Strings são sequências de caracteres, como palavras ou frases.

# Concatenação de Strings (juntar strings)
saudacao = "Olá, " + "mundo!"
print(saudacao)

# Métodos de Strings
# Vamos transformar tudo em maiúsculas e depois em minúsculas.
texto_maiusculo = texto.upper()
print("Texto em maiúsculas:", texto_maiusculo)

texto_minusculo = texto.lower()
print("Texto em minúsculas:", texto_minusculo)

# Exercício 2: Cria a tua própria string e experimenta com os métodos upper e lower.
