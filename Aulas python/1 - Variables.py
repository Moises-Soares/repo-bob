# O que é uma variável?
# Uma variável é como uma caixa onde podemos guardar coisas (como números, texto, etc.).
# Podemos usar estas caixas para manter as nossas coisas organizadas e usá-las mais tarde no nosso código.

# Exemplo 1: Criando uma variável
# Vamos criar uma variável chamada 'idade' e guardar o número 10 nela.
idade = 10

# Agora, vamos imprimir o valor da variável 'idade'.
print("A idade é:", idade)

