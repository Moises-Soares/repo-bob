# Tipos de variáveis
# Existem diferentes tipos de variáveis em Python. Os mais comuns são:
# - int (números inteiros, como 5, -3, 42)
# - float (números decimais, como 3.14, -0.001)
# - str (texto, como "Olá, mundo!")

# Exemplo 2: Tipos de variáveis
numero = 5          # Isto é um int.
decimal = 5.7       # Isto é um float.
texto = "Python"    # Isto é um str.

# Vamos imprimir os tipos destas variáveis.
print("Tipo de 'numero':", type(numero))
print("Tipo de 'decimal':", type(decimal))
print("Tipo de 'texto':", type(texto))

